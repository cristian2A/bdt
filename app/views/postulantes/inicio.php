            <h1>BOLSA DE TRABAJO</h1>
        </header>

    <p>La Secretaría de Asuntos Universitarios de la Universidad Tecnológica Nacional Facultad Regional Rosario, te invita a ingresar tu CV en el Sistema Virtual de Búsqueda de Empleo para que encuentres el trabajo o la pasantía que estás buscando.
    <br>
    Esta es una base en la cual podes actualizar  tus datos cuantas veces quieras (y es recomendable que lo hagas periódicamente) e inscribirte a las ofertas que proponen las empresas o instituciones.
    </p>

    <p class="subtitulo">¿Como Inscribirte?</p>
    <p>
    El primer paso es tener una cuenta de email de la UTN-FRRO, si aun no la tenés retirá el formulario en la Secretaría de Asuntos Universitarios.
    </p>

    <p class="subtitulo">¿Cómo acceder al sistema de Bolsa de Trabajo?</p>
    <p>Debes ingresar al sistema mediante el formulario que se encuentra más arriba, en esta misma pagina, introduciendo tu usuario y tu password.
    <br>Recordá que primero debiste haber cambiado la clave por defecto de la cuenta de correo @frro.utn.edu.ar.
    </p>

    <p class="subtitulo">¿Cómo subir tu CV, y actualizar información personal y académica?</p>
    <p>La primera vez que accedes al sistema tendrás que cargar tus datos desde "Actualizar mis datos".
    <br>
    El CV (en formato pdf, doc o archivo comprimido), lo subirás desde la pestaña "Otros Datos".
    <br>
    IMPORTANTE: Sí no tenes subido el CV al momento de postularte, tu postulación no será tenida en cuenta por el sistema.
    </p>
    

    <p class="subtitulo">¿Cómo postularte a las ofertas laborales?</p>
    <p>
    Desde "Búsquedas" tendrás un listado con todas las ofertas laborales publicadas, y para ver que solicita cada una de ellas tenes que hacer clic donde está la lupa (sobre el margen derecho).
    </p>
    

    <p>Si te interesa esa busqueda haces click en  "Postularme a esta búsqueda".</p>
    <p>Repetimos: Es imprescindible haber cargado tu curríulum en el sistema previo a postularte para las búsquedas.
    </p>
    <p>
    Una vez echo esto hace clic donde dice volver, y podes acceder a alguna otra búsqueda o salir del sistema.
    En la fila correspondiente a la búsqueda a la cual te postulaste deberá aparecer la fecha de postulación.
    </p>
    

    <p class="subtitulo">¿Cada cuanto tiempo se publican búsquedas laborales?</p>

    <p>A medida que las empresas soliciten personal, la SAU publicará las búsquedas en el sistema una vez verificada la documentación de la misma.</p>

    <p class="subtitulo">Tipos de BÚSQUEDAS:</p>
    <blockquote class="cita">
        <h3> BOLSA DE TRABAJO</h3>
        <p>Denominamos así a las búsquedas de personal para contratación en relación de dependencia.<br>
        Para este tipo de búsqueda, la condición de ALUMNO REGULAR no es excluyente, a menos que sea parte de los requerimientos de la Empresa.<br> Por supuesto que sí deberá cumplir con el requisito de ser alumno o graduado de la facultad.
    </p>
    </blockquote>
    
    <blockquote class="cita">
    <h3>PASANTÍAS</h3>
    <p>Denominamos así a las búsquedas de Pasantes.
        <br>
        Para este tipo de búsqueda, es REQUISITO (Ley Nacional Nº26427/2008) ser ALUMNO REGULAR .
    Es decir haber aprobado 2 MATERIAS en el ciclo lectivo anterior, o 2 materias en el ciclo lectivo actual.
    <br>
    Aquellos que no cumplan con este requisito no serán tenidas en cuenta su Postulaciones en este tipo de búsquedas.
    </p>
    <footer>Más info en: PASANTÍAS</footer>
    </blockquote>
    

    <p>Como podrás ver, es muy fácil y rápido postularse a una Oferta Laboral, o de Pasantía.
        <br> Es por eso que es fundamental que tu CV siempre esté actualizado.
    </p>
        <p>Los cambios mas frecuentes son:
            <ul>
                <li> - Teléfono principal</li>
                <li> - Teléfono de mensajes o celular</li>
                <li> - Dirección de e-mail</li>
                <li> - Actualización del curriculum vitae.</li>
            </ul>
    </p>



    <p>Cualquier otra modificación ya sea en idioma, conocimientos, experiencia laboral, etc. es imprescindible notificarla y por supuesto requerirá de la actualización de tu CV. 
    </p>
    <p> 
    Contamos con tu colaboración y te recomendamos consultar nuestras propuestas laborales en Sistema Virtual de Búsqueda de Empleo.
    </p>

